func sortStringKeys(keys []string, asc bool) []string {
	sort.Strings(keys)
	if asc {
		return keys
	}
	return reverseStr(keys)
}

func reverseStr(s []string) []string {
	for i, j := 0, len(s)-1; i < j; i, j = i+1, j-1 {
		s[i], s[j] = s[j], s[i]
	}
	return s
}
